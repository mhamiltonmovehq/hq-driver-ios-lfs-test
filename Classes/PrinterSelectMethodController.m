// Customize the appearance of table view cells.
- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier];
        cell.accessoryType = UITableViewCellAccessoryDisclosureIndicator;
    }
    
    if(indexPath.row == 0)
        cell.textLabel.text = @"Manual IP Address";
    else
        cell.textLabel.text = @"Auto Detect Printer";
    
    
    return cell;
}


- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    
    if(indexPath.row == 0)
    {//manually enter an ip address
        PrinterIPController *ipCtl = [[PrinterIPController alloc] initWithStyle:UITableViewStyleGrouped];
        [self.navigationController pushViewController:ipCtl animated:YES];
    }
    else 
    {//use bonjour
        PrinterBonjourController *bonjourCtl = [[PrinterBonjourController alloc] initWithStyle:UITableViewStyleGrouped];
        [self.navigationController pushViewController:bonjourCtl animated:YES];
    }

    
}


